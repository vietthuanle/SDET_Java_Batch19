package org.example.class1;

public class VariableValues {
    public static void main(String[] args) {
        int age = 20;
        System.out.println(age);
        double num2 = 324.525;
        char text1 = 'F';
        System.out.println(num2);
        System.out.println(text1);

        age = 34;
        System.out.println(age);
        num2 = 43.35;
        text1 = 'G';
        System.out.println(num2);
        System.out.println(text1);
    }
}

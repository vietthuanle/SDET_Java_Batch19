public class E7and8TypeCastingNarrowing {
    public static void main(String[] args) {
        int num = 1001;
        byte number = (byte)num;
        System.out.println(number);

        double num1 = 10.9;
        int number1=(int)num1;
        System.out.println(number1);
    }
}
